class Candidate {
  firstName: string;
  lastName: string;
  email: string;
  phone: string;
  resume: string;

  constructor(
    firstName: string,
    lastName: string,
    email: string,
    phone: string,
    resume: string
  ) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.phone = phone;
    this.resume = resume;
  }
}
